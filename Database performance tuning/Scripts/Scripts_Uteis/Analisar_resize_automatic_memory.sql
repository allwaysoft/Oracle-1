- V$SGA_CURRENT_RESIZE_OPS: informações sobre operações de redimensionamento da SGA que estão atualmente em progresso. Um operação pode ser um crescimento ou encolhimento de componentes da SGA dinamica.

- V$SGA_RESIZE_OPS: Informações sobre os ultimos 800 operacoes de redimensionamento da SGA. Não inclui operações em progresso.

- V$SGA_DYNAMIC_COMPONENTS: Informações sobre os componentes dinamicos na SGA com informações de resumo baseada em todas as operações de redimensionamento da SGA completadas desde o startup.

- V$SGA_DYNAMIC_FREE_MEMORY: Informações sobre a quantidade de memoria livre da SGA para futuras operacoes de redimensionamento da SGA dinamica.			
		
